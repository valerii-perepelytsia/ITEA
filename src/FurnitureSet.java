class FurnitureSet {
    private Furniture chair;
    private Furniture sofa;
    private Furniture coffeeTable;

    public void setChair(Furniture chair) {
        this.chair = chair;
    }

    public void setSofa(Furniture sofa) {
        this.sofa = sofa;
    }

    public void setCoffeeTable(Furniture coffeeTable) {
        this.coffeeTable = coffeeTable;
    }

    public Furniture getChair() {
        return chair;
    }

    public Furniture getSofa() {
        return sofa;
    }

    public Furniture getCoffeeTable() {
        return coffeeTable;
    }
}